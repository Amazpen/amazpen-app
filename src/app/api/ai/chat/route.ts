type AnySupabaseClient = any;

async function execReadOnlyQuery(sb: AnySupabaseClient, sql: string) {
  return sb.rpc("read_only_query", { sql_query: sql });
}

// ---------------------------------------------------------------------------
// Server-side monthly summary computation
// ---------------------------------------------------------------------------
async function computeMonthlySummary(
  sb: AnySupabaseClient,
  bizId: string,
  year: number,
  month: number
) {
  const monthStart = `${year}-${String(month).padStart(2, "0")}-01`;
  const nextMonth = month === 12 ? `${year + 1}-01-01` : `${year}-${String(month + 1).padStart(2, "0")}-01`;

  // 1. Daily entries aggregation
  const { data: dailyAgg } = await execReadOnlyQuery(sb,
    `SELECT
       COALESCE(SUM(total_register), 0) as total_income,
       COALESCE(SUM(labor_cost), 0) as total_labor_cost,
       COALESCE(SUM(labor_hours), 0) as total_labor_hours,
       COALESCE(SUM(discounts), 0) as total_discounts,
       COALESCE(SUM(day_factor), 0) as sum_day_factors,
       COUNT(*) as work_days
     FROM public.daily_entries
     WHERE business_id = '${bizId}'
       AND entry_date >= '${monthStart}'
       AND entry_date < '${nextMonth}'
       AND deleted_at IS NULL`
  );
  const daily = Array.isArray(dailyAgg) && dailyAgg[0] ? dailyAgg[0] : {
    total_income: 0, total_labor_cost: 0, total_labor_hours: 0,
    total_discounts: 0, sum_day_factors: 0, work_days: 0,
  };

  // 2. Invoices: food cost (goods_purchases) and current expenses
  // IMPORTANT: Use reference_date (not invoice_date) to match dashboard display!
  // Dashboard filters by reference_date — this ensures AI numbers = app numbers.
  const { data: invoiceAgg } = await execReadOnlyQuery(sb,
    `SELECT
       COALESCE(SUM(CASE WHEN s.expense_type = 'goods_purchases' THEN i.subtotal ELSE 0 END), 0) as food_cost,
       COALESCE(SUM(CASE WHEN s.expense_type = 'current_expenses' THEN i.subtotal ELSE 0 END), 0) as current_expenses,
       COALESCE(SUM(i.subtotal), 0) as total_expenses
     FROM public.invoices i
     JOIN public.suppliers s ON s.id = i.supplier_id
     WHERE i.business_id = '${bizId}'
       AND i.reference_date >= '${monthStart}'
       AND i.reference_date < '${nextMonth}'
       AND i.deleted_at IS NULL`
  );
  const invoices = Array.isArray(invoiceAgg) && invoiceAgg[0] ? invoiceAgg[0] : {
    food_cost: 0, current_expenses: 0, total_expenses: 0,
  };

  // 3. Goals
  const { data: goalsData } = await sb
    .from("goals")
    .select("*")
    .eq("business_id", bizId)
    .eq("year", year)
    .eq("month", month)
    .is("deleted_at", null)
    .maybeSingle();

  // 4. Business defaults
  const { data: bizData } = await sb
    .from("businesses")
    .select("name, vat_percentage, markup_percentage, manager_monthly_salary")
    .eq("id", bizId)
    .single();

  // 5. Schedule (expected work days)
  const { data: scheduleData } = await sb
    .from("business_schedule")
    .select("day_of_week, day_factor")
    .eq("business_id", bizId)
    .order("day_of_week");

  const scheduleMap = new Map<number, number>();
  if (scheduleData) {
    for (const row of scheduleData) {
      scheduleMap.set(row.day_of_week, Number(row.day_factor) || 0);
    }
  }

  // Fetch day exceptions for this month (holidays, closures)
  const { data: dayExceptions } = await sb
    .from("business_day_exceptions")
    .select("exception_date, day_factor")
    .eq("business_id", bizId)
    .gte("exception_date", monthStart)
    .lt("exception_date", nextMonth);

  const exceptionMap = new Map<string, number>();
  if (dayExceptions) {
    for (const ex of dayExceptions) {
      const dateStr = ex.exception_date.split("T")[0];
      exceptionMap.set(dateStr, Number(ex.day_factor) ?? 0);
    }
  }

  const daysInMonth = new Date(year, month, 0).getDate();
  let expectedWorkDays = 0;
  for (let d = 1; d <= daysInMonth; d++) {
    const dateStr = `${year}-${String(month).padStart(2, "0")}-${String(d).padStart(2, "0")}`;
    if (exceptionMap.has(dateStr)) {
      expectedWorkDays += exceptionMap.get(dateStr)!;
    } else {
      const dow = new Date(year, month - 1, d).getDay();
      expectedWorkDays += scheduleMap.get(dow) ?? 0;
    }
  }

  // 6. Compute everything
  const vatPct = goalsData?.vat_percentage ?? bizData?.vat_percentage ?? 0.18;
  const markup = goalsData?.markup_percentage ?? bizData?.markup_percentage ?? 1;
  const managerSalary = Number(bizData?.manager_monthly_salary) || 0;

  const totalIncome = Number(daily.total_income) || 0;
  const incomeBeforeVat = totalIncome / (1 + vatPct);
  const sumDayFactors = Number(daily.sum_day_factors) || 0;
  const workDays = Number(daily.work_days) || 0;

  // monthlyPace uses totalIncome (WITH VAT) — same as dashboard display!
  // Dashboard: dailyAverage = totalIncome / sumActualDayFactors, monthlyPace = dailyAverage * expected
  // The dashboard displays monthlyPace WITH VAT. All percentage calculations use incomeBeforeVat separately.
  const dailyAvgWithVat = sumDayFactors > 0 ? totalIncome / sumDayFactors : 0;
  const monthlyPace = dailyAvgWithVat * expectedWorkDays;
  const dailyAvg = sumDayFactors > 0 ? incomeBeforeVat / sumDayFactors : 0;
  const monthlyPaceBeforeVat = dailyAvg * expectedWorkDays;

  const managerDailyCost = expectedWorkDays > 0 ? managerSalary / expectedWorkDays : 0;
  const laborCostTotal = (Number(daily.total_labor_cost) + managerDailyCost * workDays) * markup;
  const laborCostPct = incomeBeforeVat > 0 ? (laborCostTotal / incomeBeforeVat) * 100 : 0;

  const foodCost = Number(invoices.food_cost) || 0;
  const foodCostPct = incomeBeforeVat > 0 ? (foodCost / incomeBeforeVat) * 100 : 0;

  const currentExpenses = Number(invoices.current_expenses) || 0;
  const currentExpensesPct = incomeBeforeVat > 0 ? (currentExpenses / incomeBeforeVat) * 100 : 0;

  const revenueTarget = Number(goalsData?.revenue_target) || 0;
  const targetDiffPct = revenueTarget > 0 ? ((monthlyPace / revenueTarget) - 1) * 100 : null;
  // targetDiffAmount: (pace - target) / expectedWorkDays × actualDayFactors — same as dashboard
  const targetDiffAmount = (revenueTarget > 0 && expectedWorkDays > 0)
    ? ((monthlyPace - revenueTarget) / expectedWorkDays) * sumDayFactors
    : null;

  const laborTarget = Number(goalsData?.labor_cost_target_pct) || 0;
  const laborDiffPct = laborTarget > 0 ? laborCostPct - laborTarget : null;

  const foodTarget = Number(goalsData?.food_cost_target_pct) || 0;
  const foodDiffPct = foodTarget > 0 ? foodCostPct - foodTarget : null;

  // Current expenses target: use operating_cost_target_pct if set,
  // otherwise calculate from current_expenses_target amount
  // Dashboard uses: targetAmount / monthlyPaceBeforeVat * 100
  let currentExpensesTargetPct = Number(goalsData?.operating_cost_target_pct) || 0;
  if (currentExpensesTargetPct === 0 && goalsData?.current_expenses_target) {
    const targetAmount = Number(goalsData.current_expenses_target);
    if (targetAmount > 0 && monthlyPaceBeforeVat > 0) {
      currentExpensesTargetPct = (targetAmount / monthlyPaceBeforeVat) * 100;
    }
  }
  const currentExpensesDiffPct = currentExpensesTargetPct > 0
    ? currentExpensesPct - currentExpensesTargetPct
    : 0;

  // 7. Income sources breakdown
  const { data: incomeSourcesRaw } = await execReadOnlyQuery(sb,
    `SELECT
       isr.id,
       isr.name,
       isr.income_type,
       COALESCE(SUM(dib.amount), 0) as total_amount,
       COALESCE(SUM(dib.orders_count), 0) as total_orders,
       CASE WHEN COALESCE(SUM(dib.orders_count), 0) > 0
         THEN SUM(dib.amount) / SUM(dib.orders_count)
         ELSE 0 END as avg_ticket,
       isg.avg_ticket_target
     FROM public.income_sources isr
     LEFT JOIN public.daily_income_breakdown dib ON dib.income_source_id = isr.id
       AND dib.daily_entry_id IN (
         SELECT id FROM public.daily_entries
         WHERE business_id = '${bizId}'
           AND entry_date >= '${monthStart}' AND entry_date < '${nextMonth}'
           AND deleted_at IS NULL
       )
     LEFT JOIN public.income_source_goals isg ON isg.income_source_id = isr.id
       AND isg.goal_id = (
         SELECT id FROM public.goals
         WHERE business_id = '${bizId}' AND year = ${year} AND month = ${month}
           AND deleted_at IS NULL
         LIMIT 1
       )
     WHERE isr.business_id = '${bizId}'
       AND isr.is_active = true
       AND isr.deleted_at IS NULL
     GROUP BY isr.id, isr.name, isr.income_type, isg.avg_ticket_target
     ORDER BY isr.display_order`
  );
  type IncomeSourceResult = {
    name: string;
    incomeType: string | null;
    totalAmount: number;
    totalOrders: number;
    avgTicket: number;
    avgTicketTarget: number | null;
    avgTicketDiff: number | null;
  };
  const incomeBreakdown: IncomeSourceResult[] = Array.isArray(incomeSourcesRaw)
    ? incomeSourcesRaw.map((r: {
        name: string; income_type: string | null;
        total_amount: number; total_orders: number;
        avg_ticket: number; avg_ticket_target: number | null;
      }) => {
        const target = r.avg_ticket_target != null ? Number(r.avg_ticket_target) : null;
        const avgTicket = Math.round(Number(r.avg_ticket) || 0);
        return {
          name: r.name,
          incomeType: r.income_type ?? null,
          totalAmount: Math.round(Number(r.total_amount) || 0),
          totalOrders: Number(r.total_orders) || 0,
          avgTicket,
          avgTicketTarget: target,
          avgTicketDiff: target != null ? Math.round(avgTicket - target) : null,
        };
      })
    : [];

  // 8. Managed products — fetch active products + their monthly usage
  const { data: managedProducts } = await sb
    .from("managed_products")
    .select("id, name, unit, unit_cost, target_pct")
    .eq("business_id", bizId)
    .eq("is_active", true)
    .is("deleted_at", null)
    .order("created_at")
    .limit(3);

  type ManagedProductResult = { name: string; cost: number; pct: number; targetPct: number | null; diffPct: number | null };
  const mpResults: ManagedProductResult[] = [];

  if (managedProducts && managedProducts.length > 0) {
    // Get daily_entry IDs for this month
    const { data: entryIds } = await execReadOnlyQuery(sb,
      `SELECT id FROM public.daily_entries
       WHERE business_id = '${bizId}'
         AND entry_date >= '${monthStart}' AND entry_date < '${nextMonth}'
         AND deleted_at IS NULL`
    );
    const ids = Array.isArray(entryIds) ? entryIds.map((r: { id: string }) => r.id) : [];

    for (const mp of managedProducts) {
      let totalCost = 0;
      if (ids.length > 0) {
        const idList = ids.map((r) => `'${r}'`).join(",");
        // Use SUM(quantity) × current unit_cost — same as dashboard (metrics/refresh)
        const { data: usageAgg } = await execReadOnlyQuery(sb,
          `SELECT COALESCE(SUM(quantity), 0) as total_qty
           FROM public.daily_product_usage
           WHERE daily_entry_id IN (${idList}) AND product_id = '${mp.id}'`
        );
        const totalQty = Array.isArray(usageAgg) && usageAgg[0] ? Number(usageAgg[0].total_qty) || 0 : 0;
        totalCost = totalQty * (Number(mp.unit_cost) || 0);
      }
      const pct = incomeBeforeVat > 0 ? (totalCost / incomeBeforeVat) * 100 : 0;
      const tPct = mp.target_pct != null ? Number(mp.target_pct) : null;
      mpResults.push({
        name: mp.name,
        cost: Math.round(totalCost),
        pct: Math.round(pct * 100) / 100,
        targetPct: tPct,
        diffPct: tPct != null ? Math.round((pct - tPct) * 100) / 100 : null,
      });
    }
  }

  return {
    businessName: bizData?.name || "",
    period: { year, month, monthStart, daysInMonth },
    actuals: {
      totalIncome: Math.round(totalIncome),
      incomeBeforeVat: Math.round(incomeBeforeVat),
      workDays,
      sumDayFactors: Math.round(sumDayFactors * 100) / 100,
      dailyAvg: Math.round(dailyAvgWithVat),
      monthlyPace: Math.round(monthlyPace),
      monthlyPaceBeforeVat: Math.round(monthlyPaceBeforeVat),
      expectedWorkDays: Math.round(expectedWorkDays * 100) / 100,
      totalDiscounts: Math.round(Number(daily.total_discounts)),
      totalLaborHours: Math.round(Number(daily.total_labor_hours)),
    },
    costs: {
      laborCostTotal: Math.round(laborCostTotal),
      laborCostPct: Math.round(laborCostPct * 100) / 100,
      foodCost: Math.round(foodCost),
      foodCostPct: Math.round(foodCostPct * 100) / 100,
      currentExpenses: Math.round(currentExpenses),
      currentExpensesPct: Math.round(currentExpensesPct * 100) / 100,
      currentExpensesTargetPct: Math.round(currentExpensesTargetPct * 100) / 100,
      currentExpensesDiffPct: Math.round(currentExpensesDiffPct * 100) / 100,
      // Prorated current expenses — David feedback #1, #2.
      // Fixed expenses (rent, property tax, internet) are monthly. Showing the
      // full month's invoices against partial month's income inflates the %
      // (e.g. 79.64% mid-month). Prorate by actual day-factors / expected.
      currentExpensesProrated: expectedWorkDays > 0
        ? Math.round(currentExpenses * (sumDayFactors / expectedWorkDays))
        : Math.round(currentExpenses),
      currentExpensesProratedPct: (incomeBeforeVat > 0 && expectedWorkDays > 0)
        ? Math.round(((currentExpenses * (sumDayFactors / expectedWorkDays)) / incomeBeforeVat) * 10000) / 100
        : 0,
    },
    managedProducts: mpResults,
    incomeBreakdown,
    targets: {
      revenueTarget,
      // Proportional target: target adjusted to days worked so far
      revenueTargetProportional: (revenueTarget > 0 && expectedWorkDays > 0)
        ? Math.round(revenueTarget / expectedWorkDays * sumDayFactors)
        : null,
      laborTargetPct: laborTarget,
      foodTargetPct: foodTarget,
      targetDiffPct: targetDiffPct !== null ? Math.round(targetDiffPct * 100) / 100 : null,
      targetDiffAmount: targetDiffAmount !== null ? Math.round(targetDiffAmount) : null,
      laborDiffPct: laborDiffPct !== null ? Math.round(laborDiffPct * 100) / 100 : null,
      foodDiffPct: foodDiffPct !== null ? Math.round(foodDiffPct * 100) / 100 : null,
    },
    params: { vatPct, markup, managerSalary },
    profit: (() => {
      // Profit = incomeBeforeVat - all expenses (matching P&L report exactly)
      const profitActual = incomeBeforeVat - laborCostTotal - foodCost - currentExpenses;
      // Target must also be calculated on before-VAT basis (matching P&L report)
      const revenueTargetBeforeVat = revenueTarget > 0 ? revenueTarget / (1 + vatPct) : 0;
      const laborTargetAmount = (laborTarget / 100) * revenueTargetBeforeVat;
      const foodTargetAmount = (foodTarget / 100) * revenueTargetBeforeVat;
      const currentExpensesTargetAmount = (currentExpensesTargetPct / 100) * revenueTargetBeforeVat;
      const profitTarget = revenueTargetBeforeVat > 0
        ? revenueTargetBeforeVat - laborTargetAmount - foodTargetAmount - currentExpensesTargetAmount
        : null;
      return {
        actual: Math.round(profitActual),
        actualPct: incomeBeforeVat > 0 ? Math.round((profitActual / incomeBeforeVat) * 10000) / 100 : 0,
        target: profitTarget !== null ? Math.round(profitTarget) : null,
        targetPct: profitTarget !== null && revenueTargetBeforeVat > 0
          ? Math.round((profitTarget / revenueTargetBeforeVat) * 10000) / 100
          : null,
      };
    })(),
  };
}

